describe('bundle_schema_version', () => {
  it('fails with unsupported version', async () => {
    const bundle: VerificationBundle = {
      bundle_schema_version: 99,
      status: 'confirmed',
      event_hash: TEST_EVENT_HASH,
    };
    const result = await verifyBundle(bundle);
    expect(result.verdict).toBe('fail');
    const versionCheck = result.checks.find((c) => c.check === 'bundle_schema_version');
    expect(versionCheck?.status).toBe('fail');
    expect(versionCheck?.details).toContain('unsupported bundle version: 99');
  });

  it('passes for version 1', async () => {
    const fetchMock = mockSolanaRpc({
      tx: {
        logMessages: [`Program log: Memo (len 64): "${TEST_SINGLE_LEAF_ROOT}"`],
      },
    });
    const result = await runWithFetch(makeTier1Bundle(), fetchMock);
    const versionCheck = result.checks.find((c) => c.check === 'bundle_schema_version');
    expect(versionCheck?.status).toBe('pass');
  });
});

