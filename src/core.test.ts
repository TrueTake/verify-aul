/**
 * verifyBundle integration tests.
 *
 * Covers:
 * - Happy path: full valid Tier 2 bundle → verdict: 'pass'
 * - Happy path: Tier 1 bundle (no event/signature fields) → verdict: 'pass'
 * - Tier 2 with single-leaf Merkle tree
 * - Edge: empty anchors array → fail
 * - Error: tampered event hash
 * - Error: tampered Merkle sibling
 * - Error: unsupported bundle_schema_version
 * - Error: pending bundle (no merkle_proof/anchors) → fail (no anchors)
 * - Solana: memo content mismatch (via mocked fetch)
 * - Solana: RPC returns null transaction
 * - Ed25519: bad signature
 * - Ed25519: signing key not found in signing_keys
 *
 * TSA token verification is covered in tsa.test.ts.
 * Real Solana devnet fetch is gated behind SOLANA_DEVNET_INTEGRATION.
 */

