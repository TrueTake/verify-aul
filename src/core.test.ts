// Integration: real Solana devnet (gated)
// ---------------------------------------------------------------------------

describe.skipIf(!process.env['SOLANA_DEVNET_INTEGRATION'])('Solana devnet integration', () => {
  it('fetches a real devnet transaction and verifies memo', async () => {
    const sig = process.env['SOLANA_TEST_SIG'];
    const root = process.env['SOLANA_TEST_ROOT'];
    if (!sig || !root) {
      throw new Error(
        'SOLANA_DEVNET_INTEGRATION requires SOLANA_TEST_SIG and SOLANA_TEST_ROOT env vars',
      );
    }

    // Build a single-leaf bundle for this root
    const { sha256 } = await import('@noble/hashes/sha2.js');
    const { bytesToHex, hexToBytes } = await import('@noble/hashes/utils.js');
    function leafHash(h: string): string {
      const hf = sha256.create();
      hf.update(new Uint8Array([0x00]));
      hf.update(hexToBytes(h));
      return bytesToHex(hf.digest());
    }
    const singleRoot = leafHash(root);

    const bundle: VerificationBundle = {
      bundle_schema_version: 1,
      status: 'confirmed',
      event_hash: root, // treating root as the event hash for this test
      merkle_proof: { leaf_index: 0, siblings: [], root: singleRoot },
      anchors: [{ type: 'solana', signature: sig, cluster: 'devnet' }],
      partial_anchors_reason: [],
    };

    const result = await verifyBundle(bundle, {
      solanaRpcUrl: 'https://api.devnet.solana.com',
    });
    const solanaCheck = result.checks.find((c) => c.check === 'anchor:solana');
    expect(solanaCheck?.status).toBe('pass');
  });
});
