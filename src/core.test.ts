describe('Tier 2 bundle (has event + server_signature)', () => {
  it('passes with valid canonical hash and Ed25519 signature', async () => {
    const fetchMock = mockSolanaRpc({
      tx: { logMessages: [`Program log: Memo (len 64): "${TEST_SINGLE_LEAF_ROOT}"`] },
    });
    const result = await runWithFetch(makeTier2Bundle(), fetchMock);
    expect(result.verdict).toBe('pass');
    const canonCheck = result.checks.find((c) => c.check === 'canonical_recompute');
    expect(canonCheck?.status).toBe('pass');
    const sigCheck = result.checks.find((c) => c.check === 'server_signature');
    expect(sigCheck?.status).toBe('pass');
  });

  it('fails on tampered event hash (canonical recompute mismatch)', async () => {
    const fetchMock = mockSolanaRpc({ tx: null });
    const result = await runWithFetch(makeTier2Bundle({ event_hash: 'f'.repeat(64) }), fetchMock);
    expect(result.verdict).toBe('fail');
    const canonCheck = result.checks.find((c) => c.check === 'canonical_recompute');
    expect(canonCheck?.status).toBe('fail');
    expect(canonCheck?.details).toContain('hash mismatch');
  });

  it('fails when signing_key_id not found in signing_keys', async () => {
    const fetchMock = mockSolanaRpc({ tx: null });
    const result = await runWithFetch(
      makeTier2Bundle({ signing_key_id: 'nonexistent-key' }),
      fetchMock,
    );
    expect(result.verdict).toBe('fail');
    const sigCheck = result.checks.find((c) => c.check === 'server_signature');
    expect(sigCheck?.status).toBe('fail');
    expect(sigCheck?.details).toContain('nonexistent-key');
  });

  it('fails with a bad Ed25519 signature', async () => {
    const fetchMock = mockSolanaRpc({ tx: null });
    const result = await runWithFetch(
      makeTier2Bundle({ server_signature: 'b'.repeat(128) }),
      fetchMock,
    );
    expect(result.verdict).toBe('fail');
    const sigCheck = result.checks.find((c) => c.check === 'server_signature');
    expect(sigCheck?.status).toBe('fail');
  });
});

