describe('pending bundle', () => {
  it('returns partial when status is pending and no merkle_proof/anchors', async () => {
    const bundle: VerificationBundle = {
      bundle_schema_version: 1,
      status: 'pending',
      event_hash: TEST_EVENT_HASH,
    };
    const result = await verifyBundle(bundle);
    expect(result.verdict).toBe('partial');
    expect(result.checks.some((c) => c.check === 'anchoring' && c.status === 'skip')).toBe(true);
  });

  it('returns fail when pending Tier 2 bundle has tampered event (canonical recompute mismatch)', async () => {
    const bundle: VerificationBundle = {
      bundle_schema_version: 1,
      status: 'pending',
      event_hash: TEST_EVENT_HASH,
      event: { tampered: true } as Record<string, unknown>,
      server_signature: 'a'.repeat(128),
      signing_key_id: 'fp12345678901234',
      signing_keys: [
        {
          fingerprint: 'fp12345678901234',
          public_key_base64url: 'AAAA',
          status: 'active' as const,
        },
      ],
    };
    const result = await verifyBundle(bundle);
    expect(result.verdict).toBe('fail');
    expect(
      result.checks.some((c) => c.check === 'canonical_recompute' && c.status === 'fail'),
    ).toBe(true);
  });
});

